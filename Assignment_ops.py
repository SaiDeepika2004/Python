# Assignment operators (=,+=,-=,*=,/=,%=,**=,//=)
num1 = 5
num1 += 10 # num1 = num1 + 10
print(num1)
num1 = 5  
num1 -= 4  # num1 = num1 -4
print(num1)
num1 = 5
num1 *= 2 # num1 = num1 * 2
print(num1)
num1 = 5
num1 /=2 # num1 = num1 / 2
print(num1)
num1 = 5
num1 %= 5  # num1 = num1 % 5
print(num1)
num1 = 5
num1 **= 2 # num1 = num1 ** 2
print(num1)
num1 = 5
num1 //= 2 # num1 = num1 // 2
print(num1)